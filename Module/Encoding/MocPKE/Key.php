namespace MOC\Module\Encoding\MocPKE;
/**
 *
 */
class Key {
	private $Modulo;
	private $Key;

	/**
	 * @param $Modulo
	 * @param $Key
	 */
	function __construct( $Modulo, $Key ) {
		$this->Modulo = $Modulo;
		$this->Key = $Key;
	}

	function Modulo() {
		return $this->Modulo;
	}
	function Key() {
		return $this->Key;
	}
}
